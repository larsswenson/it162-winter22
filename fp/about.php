<p>On how he started cutting heads:</p>

<p>
Oh, it’s been since the early 90s. I was born in Mexico and I was there until like 20 years old, then I came to Montreal, Canada and I started sweeping hair in a shop… eventually, slowly, that’s how I started cutting.
I had to keep on moving, so I came to Seattle in ‘94. At that point, Seattle was just appearing on the world's radar during the grunge days, so when I got here everything was pretty affordable. I met the co-founder of Rudy’s barber Shop, David Petersen, and he gave me a job and I've supported myself with hair ever since. I’ve never done anything else. This is the only thing I know how to do. If I broke my hand and tried to send out my resume… (Laughs) I don’t know what I’d do.
</p>

<p>On evolution:</p>

<p>
I absolutely feel that I am still evolving, because the times evolve. The older I get, the more I realize that everything sort of comes around. I see things that I was doing like 15 years ago, but now I’m doing it on an 11 year old, when back then I was doing it on a 24 year old. Things are always kind of evolving and stretching in a weird way.
</p>

<p>On communication:</p>

<p>
When people come in, they usually have an idea of what they want but they don’t know how to put it into words. Often they refer to pictures, which is great. Sometimes they refer to the numbers the clipper guards have, because that’s the only thing they know. I try to teach them that they don’t need to talk about numbers. 
There’s a way you talk to certain people when they’re in your chair. You don’t refer to the bangs, you refer to the front. You never mention, “Oh, this is going to be a fun haircut.” No. This is a good haircut. Maybe you don’t talk about shine, you talk about sheen, about polish, you talk about… words you find in a hardware store. 
</p>

<p>On his tools:</p>

<p>
The tools are there to help me achieve something. You know, the way you use tools and approach them is in some way respecting them. They’re extensions of your hands, all the tools you have. So the moment you grab them, you have to transmit everything into the tool in how you are going to use it.  
Scissors, clippers, comb, hairdryer, straight razor… what else do I have? That’s it, I guess. That’s all you need… just keep the scissors sharp and get new combs every now and then. Combs are like toothbrushes. You don’t want to use the same one every time. They perform better brand new.
</p>

<p>On little old ladies:</p>

<p>
I met this lady once in the airport and she was this old, really cute lady. We struck up a conversation and she asked me what I do, so I told her and out of the blue, she told me, “Oh, hair’s like a flower.” I replied, “Uh, what do you mean?” And she said, “Everyday I wake up and it’s different.” I just imagined all those years of her doing her hair… imagine, like, you know in the 40s and 50s? I will never forget that.
</p>

